"""Summarize cross-seed parameter stability for paper parameterization runs.

This script focuses on two stability questions across multiple seeds:

1. Basin-wise parameter stability:
   For each method/parameter, compute the variance of the predicted parameter
   across seeds for the same basin, then average those basin variances.

2. Attribute-correlation stability:
   For each method/attribute/parameter, compute the seed-wise Spearman
   correlation between predicted parameters and basin attributes, then summarize
   cross-seed agreement using:
   - mean R^2
   - variance of R^2
   - sign consistency of rho
   - top-k overlap of the strongest attributes

The script expects the seed-level parameter table generated by
``project.parameterize.analyze_param_results`` plus the basin attribute table.
"""

