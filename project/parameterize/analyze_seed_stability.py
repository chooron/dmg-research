        "",
        "## Overall correlation R^2 stability by method",
        "",
        variant_correlation_summary.to_string(index=False),
        "",
        "## Sample-count validation",
        "",
        json.dumps(sample_count_summary, indent=2),
        "",
        f"## Basin variance summary for `{focus_parameter}`",
        "",
        focus_parameter_variance.to_string(index=False),
        "",
        f"## Lowest R^2 variance pairs for `{focus_parameter}`",
        "",
        focus_parameter_correlations.head(15).to_string(index=False),
        "",
        "## Key-parameter correlation stability overview",
        "",
        key_parameter_view.to_string(index=False),
        "",
        "## Cross-method consistency across key parameters",
        "",
        method_consistency.to_string(index=False),
        "",
        "## Overall cross-method consistency summary",
        "",
        method_consistency_overall_summary.to_string(index=False),
        "",
